#ifndef ADAS_TYPES_H
#define ADAS_TYPES_H
//unsigned type
//unsigned types
typedef unsigned long uint32_t;         /**< type mapping unsigned 4 byte */
typedef unsigned int uint16_t;          /**< type mapping unsigned 2 byte */
typedef unsigned char uint8_t;          /**< type mapping unsigned 1 byte */

//signed types
typedef signed long sint32_t;           /**< type mapping signed 4 byte */
typedef signed int sint16_t;            /**< type mapping signed 2 byte */
typedef signed char sint8_t;            /**< type mapping signed 1 byte */


//float types
typedef float float32_t;                /**< type mapping 4 byte floating value */
typedef double float64_t;               /**< type mapping 8 byte floating value */

#endif /*ADAS_TYPES_H*/
